#include "libftprintf.h"

int	ft_putnbr(int n)
{
	
	if (n == -2147483648)
		n = write(1, "-2147483648", 11);
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	else
		n = n;
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		n = n % 10;
	}
	if (n < 10)
		n = ft_putchar(n + '0');
	return (n);
}